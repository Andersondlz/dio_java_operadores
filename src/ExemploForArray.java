public class ExemploForArray {
   public static void main(String[] args) {
    String alunos [] = {"Anderson da Luz","Marheus Augusto da Luz","Vanessa Angelica da Luz","Olandir da Luz","Ani Maria da Luz"};

    for (int indice = 0; indice < alunos.length; indice++){
        System.out.println("O aluno da listagem se chama: " + alunos[indice]);
    }

   } 
}
